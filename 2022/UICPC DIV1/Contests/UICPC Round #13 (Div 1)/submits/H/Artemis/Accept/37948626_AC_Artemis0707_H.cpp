#include <bits/stdc++.h>
using namespace std;
typedef long long ll;

int main(){
	ll n;
	cin >> n;
	ll ans = 0;
	for(ll i=n-1;i>=0;i--){
		char in;
		cin >> in;
		if(in == 'Z'){
			ans |= (1LL<<i);
		}
	}
	cout<<(1LL<<n)-1 - ans <<endl;
	return 0;
}